(function () {
    'use strict';

    function concat(...args) { return args.join(''); }

    // 1. Memory Basics
    window.APP.register({
        subject: 'os',
        unit: 3,
        id: 'memory-intro',
        title: 'Memory Management',
        icon: '🧠',
        description: 'Logical/Physical Address, Fragmentation',

        content() {
            return concat(
                '<div class="topic-header">',
                    '<div class="topic-breadcrumb"><span>Operating Systems</span> &rsaquo; Unit III</div>',
                    '<h1>🧠 Memory Management</h1>',
                '</div>',

                '<div class="theory-section">',
                    '<h2>Logical vs Physical Address Space</h2>',
                    '<p><strong>Logical Address:</strong> Generated by the CPU; also referred to as virtual address. The user program deals only with logical addresses.</p>',
                    '<p><strong>Physical Address:</strong> Address seen by the memory unit (actual RAM). The <strong>MMU (Memory Management Unit)</strong> is the hardware device that maps logical to physical addresses at runtime.</p>',
                    
                    '<h2>Fragmentation</h2>',
                    '<p>As processes are loaded and removed from memory, free memory space is broken into little pieces.</p>',
                    '<ul>',
                        '<li><strong>External Fragmentation:</strong> Total memory space exists to satisfy a request, but it is not contiguous. (Solution: Compaction or Paging).</li>',
                        '<li><strong>Internal Fragmentation:</strong> Allocated memory may be slightly larger than requested memory. The size difference is wasted memory internal to a partition.</li>',
                    '</ul>',
                '</div>'
            );
        }
    });

    // 2. Paging and Segmentation
    window.APP.register({
        subject: 'os',
        unit: 3,
        id: 'paging-seg',
        title: 'Paging & Segmentation',
        icon: '📄',
        description: 'Non-contiguous memory allocation techniques',

        content() {
            return concat(
                '<div class="topic-header">',
                    '<div class="topic-breadcrumb"><span>Operating Systems</span> &rsaquo; Unit III</div>',
                    '<h1>📄 Paging & Segmentation</h1>',
                '</div>',

                '<div class="theory-section">',
                    '<h2>Paging</h2>',
                    '<p>Paging solves external fragmentation by allowing a process\'s physical address space to be non-contiguous. Physical memory is divided into fixed-sized blocks called <strong>Frames</strong>, and logical memory into blocks of the same size called <strong>Pages</strong>. A Page Table maps pages to frames.</p>',
                    
                    '<h2>Segmentation</h2>',
                    '<p>Segmentation supports the user\'s view of memory. A program is a collection of segments (main program, procedures, arrays, stack). Each segment has a name/number and a length.</p>',
                '</div>'
            );
        }
    });

    // 3. Virtual Memory
    window.APP.register({
        subject: 'os',
        unit: 3,
        id: 'virtual-memory',
        title: 'Virtual Memory',
        icon: '🌌',
        description: 'Demand Paging, Page Replacement (FIFO, LRU)',

        content() {
            return concat(
                '<div class="topic-header">',
                    '<div class="topic-breadcrumb"><span>Operating Systems</span> &rsaquo; Unit III</div>',
                    '<h1>🌌 Virtual Memory</h1>',
                '</div>',

                '<div class="theory-section">',
                    '<h2>Concept</h2>',
                    '<p>Virtual Memory is a technique that allows the execution of processes that are not completely in memory. It provides the illusion of a massive main memory (RAM) by using the hard disk to store parts of a process.</p>',
                    
                    '<h2>Demand Paging & Thrashing</h2>',
                    '<p><strong>Demand Paging:</strong> Bring a page into memory only when it is needed. If a required page is not in RAM, a <strong>Page Fault</strong> occurs.</p>',
                    '<p><strong>Thrashing:</strong> A state where a system spends more time swapping pages in and out of memory than actually executing the process (due to high page fault rate).</p>',
                '</div>',

                '<div class="interactive-area">',
                    '<h3>Page Replacement Simulator (FIFO vs LRU)</h3>',
                    '<p>Reference String: <strong>1, 2, 3, 4, 1, 2</strong> (3 Frames available)</p>',
                    '<div class="controls-row">',
                        '<button class="btn btn-primary" onclick="simPage(\'fifo\')">FIFO Algorithm</button>',
                        '<button class="btn btn-secondary" onclick="simPage(\'lru\')">LRU Algorithm</button>',
                    '</div>',
                    '<div id="page-result" class="result-display" style="font-family: monospace; min-height:100px;"></div>',
                '</div>'
            );
        },
        init() {
            window.simPage = function(algo) {
                const res = document.getElementById('page-result');
                if (algo === 'fifo') {
                    res.innerHTML = `<strong>FIFO (First In First Out):</strong>\nString: 1, 2, 3, 4, 1, 2\nPage Faults: 6 (Every request misses, replacing the oldest loaded page)`;
                } else {
                    res.innerHTML = `<strong>LRU (Least Recently Used):</strong>\nString: 1, 2, 3, 4, 1, 2\nPage Faults: 6 (Every request misses, replacing the least recently accessed page)`;
                }
            };
        }
    });

})();
